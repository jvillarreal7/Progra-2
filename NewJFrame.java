    private void pDado()
    {
        int suma1 = 0, suma2 = 0;
        for (int i = 1; i <= 5; i++)
        {
            int x = (int)(Math.random()*6+1);
            this.txtOutput.append("Turno "+i+"/5!\nJugador 1 avento el dado!\nCayo en: "+x+"!\n");
            suma1+=x;
            int y = (int)(Math.random()*6+1);
            this.txtOutput.append("Jugador 2 avento el dado!\nCayo en: "+y+"!\n");
            suma2+=y;
            txtOutput.append("------------------------------\n");
        }
        if (suma1 > suma2)
        {
            this.txtOutput.append("Resultados:\nJugador 1: "+suma1+"\nJugador 2: "+suma2+"\nJugador 1 gana!\n");
        }
        if (suma1 < suma2)
        {
            this.txtOutput.append("Resultados:\nJugador 1: "+suma1+"\nJugador 2: "+suma2+"\nJugador 2 gana!\n");
        }
        if (suma1 == suma2)
        {
            this.txtOutput.append("Resultados:\nJugador 1: "+suma1+"\nJugador 2: "+suma2+"\nEmpate!\n");
        }
        txtOutput.append("------------------------------\n");
    }
