    private void aCirculo()
    {
        double radio, area;
        DecimalFormat df = new DecimalFormat("#.###");
        radio = (double)(Math.random()*100)+1;
        this.txtOutput.append("El radio de la circunferencia es de: "+df.format(radio)+"\n");
        area = Math.PI*Math.pow(radio,2);
        this.txtOutput.append("El area de la circunferencia es: \n"+df.format(area)+"\n");
        this.txtOutput.append("------------------------------\n");
    }
